  // View mode actions
  setViewMode: (mode: ViewMode) => void;
  toggleViewMode: () => void;
  setColumnSort: (sort: SortOption) => void;
  setColumnCount: (count: number) => void;
  setColumnShowOnlySelected: (enabled: boolean) => void;
  setCanvasWeekView: (enabled: boolean) => void;
